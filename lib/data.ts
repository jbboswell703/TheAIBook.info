export const getAllTools = () => {
  const allTools = []
  for (const category in tools) {
    allTools.push(...tools[category as keyof typeof tools])
  }
  return allTools
}

export const getCategoryTools = (slug: string) => {
  return tools[slug as keyof typeof tools] || []
}

export const getCategoryBySlug = (slug: string) => {
  return categories.find((category) => category.slug === slug)
}

export const getToolCount = (slug: string) => {
  const categoryTools = tools[slug as keyof typeof tools]
  return categoryTools ? categoryTools.length : 0
}

